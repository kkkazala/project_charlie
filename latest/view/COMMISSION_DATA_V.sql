CREATE OR REPLACE VIEW WKSP_CHARLIEDEV.COMMISSION_DATA_V AS
SELECT
  BV.BET_AMOUNT,
  BV.BET_MULTIPLIER,
  BV.BET_TYPE,
  BV.PROMOTION_NAME,
  BV.PROMOTION_MULTIPLIER,
  BV.SPORTS_LEAGUE_ID,
  BV.BET_DATE,
  PR.DATE_FROM,
  PR.DATE_TO,
  MD.FORMULA
FROM BETS_V BV
JOIN PROGRAMS PR ON (PR.ORGANIZATION_ID = BV.SPORTS_LEAGUE_ID)
JOIN MODELS MD ON (MD.ID = PR.MODEL_ID);